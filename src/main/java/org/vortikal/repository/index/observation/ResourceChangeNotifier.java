package org.vortikal.repository.index.observation;

/**
 * This interface should be implemented by classes that notify observers
 * of changes to resources. It allowes the observers to register and de-register
 * themselves from the notifying service. The advantage of this is that observers
 * of a single notifier instance do not have to be configured at a central place.
 * (They only need a reference to the typically global notifier, then register 
 * themselves for callback at init)
 *
 * @author oyviste
 */
public interface ResourceChangeNotifier {
    
    public void pollChanges();
    
    public boolean registerObserver(ResourceChangeObserver observer);
    
    public boolean unregisterObserver(ResourceChangeObserver observer);
    
}
