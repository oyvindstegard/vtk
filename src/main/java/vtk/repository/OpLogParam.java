package vtk.repository;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotation for a parameter which should be logged in the operation log.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.PARAMETER)
public @interface OpLogParam {
    /**
     * Parameter name, default is empty.
     * 
     * <p>If specified, the name of the parameter will be logged in addition to its call-time value.
     * @return operation parameter name, or empty string if unnamed
     */
    String name() default "";
}
